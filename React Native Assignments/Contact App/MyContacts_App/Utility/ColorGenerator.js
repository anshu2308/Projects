    '#ff0000',
    '#00ff00',
    '#0000ff',
    '#ffff00',
    '#ff00ff',
    '#00ffff',
    '#990000',
    '#009900',
    '#000099',
    '#999900',
    '#990099',
    '#009999',
    '#999999',
    '#666666',
    '#333333',
    '#ffffff',
    '#cccccc',
    '#9999cc',
    '#6666cc',
    '#3333cc',
    '#cc9999',
    '#cc6666',
    '#cc3333',
    '#99cc99',
    '#996666',
    '#993333',
